    <h1 class="offset-md-1 col-md-10 text-success border-bottom border-success">A Propos de VegMd</h1>

    <div class="offset-md-1 col-md-10">
        Cette application a été créée pour trouver plus facilement un médecin étant en accord avec un mode de vie vegan.<br>
        Car ce n'est pas toujours évident d'avoir en face de soi quelqu'un qui comprend que les protéines ne se trouvent pas uniquement dans les animaux<br>
        Ce n'est également pas évident de trouver un spécialiste pour qui le calcium peut se trouver hors du lait.<br>

        <h2 class="text-success border-bottom border-success">Les médecins semblent avoir une localisation différente de l'adresse</h2>
        Si l'épingle semble éloignée de la ville, zoomer sur l'épingle pour avoir une vue plus précise.<br>
        La localisation GPS reste cependant approximative et est à but indicative.<br>

        <h2 class="text-success border-bottom border-success">Qui peut ajouter un médecin?</h2>
        Tout le monde. Ou presque...<br>
        Cette carte est collaborative, pas besoin d'être médecin pour s'ajouter à la liste.<br>
        Si votre médecin est tolérant vis-à-vis de votre régime alimentaire, vous pouvez l'ajouter.<br>
        <div class="alert alert-success">
            Il manque une catégorie de spécialistes?<br>
            Prenez contact et proposez nous la spécialisation manquante.<br>
            Si nous la jugeons pertinante, elle viendra compléter nos informations.
        </div>

        <h2 class="text-success border-bottom border-success">Des informations sont erronnées?</h2>
        Dans ce cas, vous pouvez les signaler.<br>
        Suivant les informations données, nous modifierons ou supprimerons les données.
        <div class="alert alert-warning col-md-12">
            Vous êtes médecin et préférez ne pas voir votre nom ici?<br>
            Prenez contact, discutons du pourquoi et voyons si vous pouvez nous laisser une chance.
        </div>


